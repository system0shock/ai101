class PdfWriter:
    def __init__(self) -> None:
        self.doc = fitz.open()
        self.page: fitz.Page | None = None
        self.y = MARGIN_TOP
        self.page_no = 0
        self.font_regular = fitz.Font(fontfile=str(FONT_REG))
        self.font_bold = fitz.Font(fontfile=str(FONT_BOLD))

    def new_page(self) -> None:
        self.page = self.doc.new_page(width=PAGE_W, height=PAGE_H)
        self.page_no += 1
        self.page.insert_font(fontname="Arial", fontfile=str(FONT_REG))
        self.page.insert_font(fontname="Arial-Bold", fontfile=str(FONT_BOLD))
        self.page.insert_font(fontname="Arial-Italic", fontfile=str(FONT_ITALIC))
        if self.page_no > 1:
            self.page.insert_text(
                (MARGIN_X, PAGE_H - 28),
                f"AI 101 / Part 1 speaker notes / updated 2026-05-21 / {self.page_no}",
                fontname="Arial",
                fontsize=8,
                color=MUTED,
            )
        self.y = MARGIN_TOP

    def ensure(self, height: float) -> None:
        if self.page is None:
            self.new_page()
        elif self.y + height > PAGE_H - MARGIN_BOTTOM:
            self.new_page()

    def text_width(self, text: str, size: float, bold: bool = False) -> float:
        font = self.font_bold if bold else self.font_regular
        return font.text_length(text, fontsize=size)

    def wrap(self, text: str, size: float, width: float, bold: bool = False) -> list[str]:
        words = text.split()
        lines: list[str] = []
        cur = ""
        for word in words:
            candidate = word if not cur else f"{cur} {word}"
            if self.text_width(candidate, size, bold) <= width:
                cur = candidate
                continue
            if cur:
                lines.append(cur)
            if self.text_width(word, size, bold) <= width:
                cur = word
            else:
                cur = ""
                piece = ""
                for ch in word:
                    if self.text_width(piece + ch, size, bold) <= width:
                        piece += ch
                    else:
                        if piece:
                            lines.append(piece)
                        piece = ch
                cur = piece
        if cur:
            lines.append(cur)
        return lines

    def line(self, text: str, size: float = 10.5, bold: bool = False, color=INK, indent: float = 0, gap: float = 2) -> None:
        lh = size * 1.32
        self.ensure(lh + gap)
        assert self.page is not None
        self.page.insert_text(
            (MARGIN_X + indent, self.y),
            text,
            fontname="Arial-Bold" if bold else "Arial",
            fontsize=size,
            color=color,
        )
        self.y += lh + gap

    def paragraph(
        self,
        text: str,
        size: float = 10.5,
        color=INK,
        indent: float = 0,
        first: str = "",
        bold: bool = False,
        gap_after: float = 7,
    ) -> None:
        width = TEXT_W - indent
        lines = self.wrap((first + text).strip(), size, width, bold)
        lh = size * 1.34
        self.ensure(lh * len(lines) + gap_after)
        for line in lines:
            self.line(line, size=size, bold=bold, color=color, indent=indent, gap=0)
        self.y += gap_after

    def bullets(self, items: Iterable[str], size: float = 10.2) -> None:
        for item in items:
            lines = self.wrap(item, size, TEXT_W - 18)
            self.ensure(size * 1.35 * len(lines) + 4)
            self.line("• " + lines[0], size=size, indent=0, gap=0)
            for rest in lines[1:]:
                self.line(rest, size=size, indent=18, gap=0)
            self.y += 3

    def h1(self, text: str) -> None:
        self.ensure(58)
        self.line(text, size=20, bold=True, color=ACCENT, gap=9)

    def h2(self, text: str) -> None:
        self.ensure(42)
        self.y += 4
        self.line(text, size=15, bold=True, color=INK, gap=5)

    def label(self, text: str, color=MUTED) -> None:
        self.line(text.upper(), size=8.3, bold=True, color=color, gap=3)

    def rule(self) -> None:
        self.ensure(12)
        assert self.page is not None
        self.page.draw_line((MARGIN_X, self.y), (PAGE_W - MARGIN_X, self.y), color=(0.82, 0.82, 0.82), width=0.6)
        self.y += 12

    def save(self, path: Path) -> None:
        self.doc.set_metadata(
            {
                "title": "AI 101 на GigaCode — Part 1 speaker notes, updated",
                "author": "Generated from presentation-part1.html",
                "subject": "Updated speaker notes for Part 1; source of truth: presentation Part 1",
                "keywords": "AI 101, GigaCode, LLM, context management, prompt engineering",
            }
        )
        self.doc.save(path, garbage=4, deflate=True)
        self.doc.close()


